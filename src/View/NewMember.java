    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // DATA BINDING CLIENTE
        Cliente cliente = new Cliente();
        String valore = null; 
        String messageError = "";
        boolean isChecked = true;
        
        jLabel11.setVisible(false);
        jLabel18.setVisible(false);
        jLabel9.setVisible(false);
        
        cliente.setId(Long.valueOf(jLabel12.getText()));
        
        valore = jTextField1.getText();
        if(!valore.equals(""))
            cliente.setNome(valore);
        else {
            messageError = "Nome,";
            isChecked = false;
        }
        
        valore = jTextField4.getText();
        if(!valore.equals(""))
            cliente.setEmail(valore);
        else {
            messageError += " Email,";
            isChecked = false;
        }
        
        valore = jTextField6.getText();
        if(!valore.equals(""))
            cliente.setLuogoDiNascita(valore);
        else {
            messageError += " luogo di nascita,";
            isChecked = false;
        }
        
        cliente.setSesso(Genere.valueOf(Genere.class,jComboBox2.getSelectedItem().toString().toUpperCase()));
        cliente.setMetodoDiPagamento(MetodoDiPagamento.valueOf(MetodoDiPagamento.class, jComboBox1.getSelectedItem().toString().toUpperCase()));

        try {
            valore = jTextField2.getText();
            if(!valore.equals(""))
                cliente.setCellulare(Long.valueOf(valore));
            else {
                messageError += " Cellulare,";
                isChecked = false;
            }
            
            valore = jTextField5.getText();
            if(!valore.equals(""))
                cliente.setDataDiNascita(Date.valueOf(cliente.dateFormat(valore))); // YYYY-MM-DD
            else {
                messageError += " Data,";
                isChecked = false;
            }
            
        } catch(NumberFormatException ex) {
            ex.printStackTrace();
            jLabel11.setVisible(true);
        } catch(IllegalArgumentException ex) {
            ex.printStackTrace();
            jLabel9.setVisible(true);
        } catch(IndexOutOfBoundsException ex) {
            ex.printStackTrace();
            jLabel9.setVisible(true);
        }
        
        
        // DATA BINDING ANAMNESI
        Anamnesi anamnesi = new Anamnesi();
        anamnesi.setCliente(cliente);
        anamnesi.setDismorfismo(jTextArea1.getText());
        anamnesi.setPatologie(jTextArea2.getText());
        
        try{
            valore = jTextField3.getText();
            if(!valore.equals(""))
                anamnesi.setFrequenzaAllenamenti(Integer.parseInt(valore));
            else {
                messageError += " Freq. allenamenti,";
                isChecked = false;
            }
            
            if(jComboBox4.getSelectedItem().toString().toUpperCase().equals("SI"))
                anamnesi.setSmoker(true);
            else
                anamnesi.setSmoker(false);

            anamnesi.setStileDiVita(StileDiVita.valueOf(StileDiVita.class, jComboBox3.getSelectedItem().toString().toUpperCase()));
        } catch(NumberFormatException ex){
            ex.printStackTrace();
            jLabel18.setVisible(true);
        }
        
        
        if(isChecked)
        {
            clienteService.insertCliente(cliente);
            clienteService.createAnamnesiCliente(anamnesi);
            JOptionPane.showMessageDialog(null,"Iscrizione cliente eseguita con successo!");
            setVisible(false);
            new NewMember().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null,"I seguenti campi sono obbligatori: " + messageError);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

